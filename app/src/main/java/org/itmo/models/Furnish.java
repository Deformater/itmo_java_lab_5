package org.itmo.models;

/**
 * The Furnish enum represents the different levels of furnishing for a property.
 */
public enum Furnish {
    /**
     * Represents a property with designer furnishing.
     */
    DESIGNER,

    /**
     * Represents a property with fine furnishing.
     */
    FINE,

    /**
     * Represents a property with bad or no furnishing.
     */
    BAD
}
