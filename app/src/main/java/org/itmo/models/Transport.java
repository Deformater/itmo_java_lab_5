package org.itmo.models;

/**
 * The Transport enum represents different levels of transportation.
 */
public enum Transport {
    /**
     * No transportation needed.
     */
    NONE,

    /**
     * Little transportation needed.
     */
    LITTLE,

    /**
     * Normal level of transportation needed.
     */
    NORMAL,

    /**
     * Sufficient transportation needed.
     */
    ENOUGH;
}
